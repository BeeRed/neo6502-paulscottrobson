; ************************************************************************************************
;
;							Write byte to memory and optionally screen
;
; ************************************************************************************************

		.section code

ASWriteByte:
		ldx 	('P'-'A')*4 + FastVariables + 0
		stx 	zTemp0
		ldx 	('P'-'A')*4 + FastVariables + 1
		stx 	zTemp0+1
		sta 	(zTemp0)

		pha
		lda 	#32
		jsr 	OSWriteScreen
		pla
		jsr 	ASPrintHex

		inc 	('P'-'A')*4 + FastVariables + 0
		bne 	_ASWBNoCarry
		inc 	('P'-'A')*4 + FastVariables + 1
_ASWBNoCarry:
		rts

; ************************************************************************************************
;
;									Write out address stuff
;
; ************************************************************************************************

ASAddress:
		lda 	('P'-'A')*4 + FastVariables + 1
		jsr 	ASPrintHex
		lda 	('P'-'A')*4 + FastVariables + 0
		jsr 	ASPrintHex
		lda 	#32
		jsr 	OSWriteScreen
		lda 	#':'
		jsr 	OSWriteScreen
		rts

; ************************************************************************************************
;
;									Write EOL for assembly
;
; ************************************************************************************************

ASEndLine:
		lda 	#13
		jsr 	OSWriteScreen
		rts

; ************************************************************************************************
;
;										Write A as 2 hex digits.
;
; ************************************************************************************************

ASPrintHex:		
		pha
		lsr 	a
		lsr 	a
		lsr 	a
		lsr 	a
		jsr 	_ASPrintNibble
		pla
_ASPrintNibble:
		and 	#15
		cmp 	#10
		bcc 	_ASNotHex
		adc 	#6
_ASNotHex:
		adc 	#48
		cmp 	#64
		bcc 	_ASNotChar
		eor 	#32
_ASNotChar:		
		jmp 	OSWriteScreen

		.send code

		
