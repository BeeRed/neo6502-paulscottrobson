		.section code	

EXPUnaryLen: ;; [LEN(]
		jsr 	EXPEvalString 					; string to R0, zTemp0		
		jsr 	ERRCheckRParen 					; )
		lda 	(zTemp0)
		ldx 	#IFR0
		jsr 	IFloatSetByte
		rts

		.send code

;: [len(string)]\
; Returns the length of the string\
