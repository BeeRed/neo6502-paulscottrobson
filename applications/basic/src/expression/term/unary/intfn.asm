		.section code	

EXPUnaryInt: ;; [int(]
		jsr 	EXPEvalNumber 					; number to R0
		jsr 	ERRCheckRParen 					; check for )
		jsr 	IFloatIntegerR0 				; force to integer.
		rts

		.send code
;: [int(n)]\
; Returns the integer part of the number, e.g. without the fractional part.\
; { print int(-4.327) , int(5.61) } prints -4 5

				
