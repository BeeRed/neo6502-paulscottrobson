		.section code	

EXPUnaryFrac: ;; [frac(]
		jsr 	EXPEvalNumber 					; number to R0
		jsr 	ERRCheckRParen 					; )
		jsr 	IFloatFractionalR0 				; get fractional bit.
		rts

		.send code

;: [frac(n)]\
; Returns the fractional part of the number, e.g. without the integer part.\
; { print frac(-4.3) , frac(5.6) } prints 0.3 0.6
				
