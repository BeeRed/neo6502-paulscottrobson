		.section code	

EXPUnaryInlineString: ;; [[[STRING]]]
		clc 								; physical address -> IM0,1
		tya
		adc 	codePtr
		sta 	IFR0+IM0
		lda 	codePtr+1
		adc 	#0
		sta 	IFR0+IM1
		stz 	IFR0+IM2 					; fill in rest
		lda 	#$80
		sta 	IFR0+IExp
		;
		tya	 								; skip over it.
		sec
		adc 	(codePtr),y
		tay
		;
		rts

_EXPUISRange:
		.error_range


		.send code
		


				
