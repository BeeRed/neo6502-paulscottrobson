; ************************************************************************************************
;
;							Output token data for constant in R0
;
; ************************************************************************************************

TOKTokeniseConstant:
		lda 	IFR0+IM0 					; check > 64
		pha 								; save on stack
		and 	#$C0
		ora 	IFR0+IM1
		ora 	IFR0+IM2
		beq 	_TTCLess
		;
		phx
		ldx 	#6 							; divide by 64
_TTCShiftRight:		
		lsr 	IFR0+IM2
		ror 	IFR0+IM1
		ror 	IFR0+IM0
		dex
		bne 	_TTCShiftRight
		plx
		jsr 	TOKTokeniseConstant
_TTCLess:
		pla
		and 	#$3F 						; lower 6 bits
		jsr 	TOKWriteA
		rts

		.send code
		
