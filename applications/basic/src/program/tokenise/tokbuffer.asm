;
;								Element buffer reset/append
;
; ************************************************************************************************

TOKResetElement:
		stz 	TOKElement
		rts

TOKWriteElement:
		phx
		ldx 	TOKElement
		sta 	TOKElementText,x
		stz 	TOKElementText+1,x
		plx
		inc 	TOKElement		
		rts		

		.send code

		.section storage
TOKElement:	
		.fill 	1
TOKElementText:		
		.fill 	255
		.send storage

