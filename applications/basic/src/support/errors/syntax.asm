;									Check next character
;
; ************************************************************************************************

		.section code	

checknext .macro
		lda 	(codePtr),y
		cmp 	#\1
		bne 	ERRSyntaxError
		iny
		rts
		.endm

ERRCheckLParen:
		.checknext 	PR_LPAREN
ERRCheckRParen:
		.checknext 	PR_RPAREN
ERRCheckComma:
		.checknext 	PR_COMMA

ERRCheckA:
		cmp 	(codePtr),y
		bne 	ERRSyntaxError
		iny
		rts		

ERRSyntaxError:
		.error_syntax

		.send code
		
