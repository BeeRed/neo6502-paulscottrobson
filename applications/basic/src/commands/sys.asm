		.section code

Command_Sys:	;; [sys]
		jsr 	EXPEvalInteger16 			; execution address
		phy

		lda 	('A'-'A')*4 + FastVariables ; load AXY
		ldx 	('X'-'A')*4 + FastVariables
		ldy 	('Y'-'A')*4 + FastVariables

		jsr 	_CSCallIFR0 				; call the code

		ply
		rts

_CSCallIFR0:
		jmp 	(IFR0+IM0)

		.send code
		
;:[sys a]
; Call machine code routine at a ; loads the A X and Y variables into the same named
; 6502 registers
; { sys $C008 }

				
