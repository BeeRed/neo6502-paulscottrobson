OSWriteStringZ:
		pha
		phx
		phy
		stx		rTemp1
		sty 	rTemp1+1
		ldx 	#255
		ldy 	#255
		bra 	OSWSLoop

; ************************************************************************************************
;
;								Write len prefixed YX string to screen
;
; ************************************************************************************************

OSWriteString:
		pha
		phx
		phy
		stx		rTemp1
		sty 	rTemp1+1
		lda 	(rTemp1)
		tax
		ldy 	#0
OSWSLoop:
		cpx 	#0
		beq 	_OSWSExit
		dex
		iny
		lda 	(rTemp1),y
		beq 	_OSWSExit
		jsr 	OSWriteScreen
		bra 	OSWSLoop
_OSWSExit:
		ply
		plx
		pla
		rts		

