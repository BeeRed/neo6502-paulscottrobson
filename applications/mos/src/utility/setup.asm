;										Initialise
;
; ************************************************************************************************

OSInitialise:
		lda 	#40 						; 40 x 30 display
		sta 	OSXSize
		lda 	#30
		sta 	OSYSize
		jsr 	OSClearScreen 				; clear the display
		jsr 	OSKeyboardInitialise 		; reset the keyboard state.
		rts

; ************************************************************************************************
;
;									Get Screen Size -> XY
;
; ************************************************************************************************

OSGetScreenSize:
		ldx 	OSXSize
		ldy 	OSYSize
		rts

; ************************************************************************************************
;
;									Get break status
;
; ************************************************************************************************		

OSCheckBreak:
		lda 	OSEscapePressed
		rts
		
