;									Screen line into buffer
;
; ************************************************************************************************

OSScreenLine:
		stz 	OSEditLength 				; clear buffer
_OSScreenLoop:		
		jsr 	OSReadKeystroke	 			; get key.
		cmp 	#$10						; insert
		beq 	_OSSInsert
		cmp 	#$07 						; delete / backspace
		beq 	_OSSDelete
		cmp 	#$08
		beq 	_OSSDelete	
		cmp 	#$0D 						; Return key ?
		beq 	_OSSReturn
		jsr 	OSWriteScreen
		bra 	_OSScreenLoop


_OSSInsert:
		.debug

_OSSDelete:
		.debug

_OSSReturn:
		.debug		

		.send code

