; ************************************************************************************************
;
;									Make R0 into an integer
;
; ************************************************************************************************

IFloatAbsoluteR0:
		lda 	IFR0+IExp					; reset the sign bit.
		and 	#IFSign ^ 255
		sta 	IFR0+IExp
		clc		
		rts		

		.send 	code

