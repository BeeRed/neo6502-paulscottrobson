		.section code

; ************************************************************************************************
;
;									Push R:X on the stack
;
; ************************************************************************************************

IFloatPushRx:
		phy
		ldy 	IFStackIndex
		lda 	IM0,x
		sta 	IFStack,y
		lda 	IM1,x
		sta 	IFStack+1,y
		lda 	IM2,x
		sta 	IFStack+2,y
		lda 	IExp,x
		sta 	IFStack+3,y
		iny
		iny
		iny
		iny
		sty 	IFStackIndex
		ply
		rts

; ************************************************************************************************
;
;									Pull R:X off the stack
;
; ************************************************************************************************

IFloatPullRx:
		phy
		ldy 	IFStackIndex
		dey
		dey
		dey
		dey

		lda 	IFStack,y
		sta 	IM0,x
		lda 	IFStack+1,y
		sta 	IM1,x
		lda 	IFStack+2,y
		sta 	IM2,x
		lda 	IFStack+3,y
		sta 	IExp,x

		sty 	IFStackIndex
		ply
		rts

		.send 	code

		.section storage
IFStackIndex:
		.fill 	1
IFStack:
		.fill 	16*4
		.send storage

