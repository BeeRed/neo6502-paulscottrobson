; ************************************************************************************************
;
;									Load R0 from register X
;
; ************************************************************************************************

IFloatCopyFromRegister:
		lda 	0,x
		sta 	IFR0
		lda 	1,x
		sta 	IFR0+1
		lda 	2,x
		sta 	IFR0+2
		lda 	3,x
		sta 	IFR0+3
		rts

; ************************************************************************************************
;
;									Save R0 to register X
;
; ************************************************************************************************

IFloatCopyToRegister:
		lda 	IFR0
		sta 	0,x
		lda 	IFR0+1
		sta 	1,x
		lda 	IFR0+2
		sta 	2,x
		lda 	IFR0+3
		sta 	3,x
		rts

		.send 	code


