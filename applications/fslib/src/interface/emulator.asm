;									Interface to Flash Hardware
;
; ************************************************************************************************

		.section code

flash 	.macro
		sta 	$CF11 						; save data
		lda 	#\1 						; activate on command write
		sta 	$CF10
		.endm

FSHErase:
		.flash 	0
		rts

FSHOpenRead:
		.flash 	1
		rts

FSHOpenWrite:
		.flash 	2
		rts

FSHRead:
		.flash 	3
		lda 	$CF12
		rts

FSHWrite:
		.flash 	4
		rts

FSHEndCommand:
		.flash 	5
		rts

		.send code
		
